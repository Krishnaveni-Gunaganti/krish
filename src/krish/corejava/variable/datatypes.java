package krish.corejava.variable;

public class datatypes {
    public static void main(String[] args) {
        int number;
        number=-69;
        System.out.println(number);
        double height=6.48d;
        System.out.println(height);
        short weight=76;
        System.out.println(weight);
        float bankBalance=5473845.87f;
        System.out.println(bankBalance);
        char enterYorN='N';
        System.out.println(enterYorN);
        byte byt=127;
        System.out.println(byt);

        long lengthOfRiver=1897L;
        System.out.println(lengthOfRiver);
        boolean doYouWantCoffee=true;
        System.out.println(doYouWantCoffee);


    }
}
